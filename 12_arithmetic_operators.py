# Arithmetic Operators in Python Programming Language
print(10 + 2)  # addition
print(10 - 2)  # subtraction
print(10 * 2)  # multiplication
print(10 / 2)  # division with result in decimal points/float numbers
print(10 // 2)  # division with result in whole number/integer numbers
print(10 % 2)  # division with result in remainder
print(10 ** 2)  # indices or power operator such as 2 power 2 is 4

# In above operators, we can also write it as augmented assignment operators.
x = 10
x = x + 3
print(x)  # 13 this is how we increment the value inside a variable.

# Let's see how can we write in 'Augmented Operator'.
# 'Augmented Operator' is a way to writing the same code but in shorter form.
x += 3
print(x)

name = "John"
name += "Smith"
print(name)
